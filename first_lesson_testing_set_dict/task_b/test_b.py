import pytest
from io import StringIO
import sys
from b import main


@pytest.mark.parametrize('input_data, answer', [
    # Примеры из условия
    ('1 2 2 10 10 10', '0.500000000000000'),
    ('4 1 2 5 5 5', '1.200000000000000'),
    ('2 3 4 7 6 5', '1.495238095238095'),
    ('1 6 3 7 6 5', '1.271428571428571'),
    ('2 3 4 10 9 2', '1.055555555555556'),

    # 2. Пример, где выгодно возвращаться домой между походами
    ("2 3 4 10 9 2\n", "1.055555555555556"),
    # 3. Проверка неравенства треугольника — путь через третью вершину короче
    ("10 10 1 10 9 8\n", "2.361111111111111"),
    # 4. Скорости одинаковые — любой маршрут одинаков по времени
    ("5 6 7 10 10 10\n", "1.800000000000000"),
    # 5. Сильное различие скоростей — выгодно оставлять вещи дома
    ("10 20 5 10 9 1\n", "5.277777777777779"),
    # 6. Минимальные значения — все расстояния и скорости равны 1
    ("1 1 1 1 1 1\n", "3.000000000000000"),
    # 7. Максимальные значения — проверка на переполнение и точность
    ("100 100 100 100 100 100\n", "3.000000000000000"),
    # 8. Очень выгодный короткий переход через другую точку
    ("100 1 1 10 9 8\n", "0.436111111111111"),
    # 9. Когда магазин и пункт почти совпадают (с = 1)
    ("50 50 1 10 5 2\n", "30.000000000000000"),
    # 10. Путь через дом явно выгоднее, чем напрямую
    ("3 3 10 10 8 5\n", "1.350000000000000"),
])
def test_h(input_data, answer):
    sys.stdin = StringIO(input_data)

    result = main()
    assert result == answer
