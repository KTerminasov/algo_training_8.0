import pytest
from io import StringIO
import sys
from d import main

@pytest.mark.parametrize('input_data, answer', [  
    # Основные примеры из условия
    ("5\n5 1 1 1 1", "1 1 2"),
    ("4\n1 2 3 4", "1 2 4"),
    
    # Крайние случаи
    ("2\n1 1", "0 1 2"),
    ("3\n1 1 1", "1 1 2"),
    ("3\n1 2 1", "0 1 3"),
    
    # Большие числа
    ("3\n1000000000 1 1000000000", "0 1 3"),
    ("4\n1000000000 1000000000 1000000000 1000000000", "0 1 4"),
    
    # Возрастающие последовательности
    ("5\n1 2 3 4 5", "1 3 4"),
    ("6\n1 2 3 4 5 6", "0 3 6"),
    
    # Убывающие последовательности  
    ("5\n5 4 3 2 1", "1 2 4"),
    ("6\n6 5 4 3 2 1", "0 1 4"),
    
    # Симметричные последовательности
    ("5\n1 2 3 2 1", "0 1 5"),
    ("7\n1 2 3 4 3 2 1", "0 1 7"),
    
    # Случаи с одним большим элементом
    ("5\n1 1 100 1 1", "0 2 4"),
    ("4\n1 100 1 1", "1 1 2"),
    
    # Минимальная разница не равна 0
    ("4\n1 2 4 8", "1 3 4"),
    ("5\n1 3 6 10 15", "1 3 4"),
    
    # Большой тест (проверка эффективности)
    ("1000\n" + "1 " * 1000, "0 1 1000"),
])
def test_h(input_data, answer):
    sys.stdin = StringIO(input_data)
    
    result = main()
    assert result == answer

